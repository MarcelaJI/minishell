#include "../includes/minishell.h"

int is_valid_token_char(char c)
{
    if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
        (c >= '0' && c <= '9') || (c == '-') || (c == '.') ||
        (c == '/') || (c == '_') || (c == ',') || (c == ':') ||
        (c == '$') || (c == '\"') || (c == '\'') || (c == '+'))
        return (1);
    return (0);
}